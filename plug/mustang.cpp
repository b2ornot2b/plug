int Mustang::stop_amp()
{
    int ret;

    if(amp_hand != NULL)
    {
        // release claimed interface
        ret = libusb_release_interface(amp_hand, 1);
        if(ret)
            return ret;

        // close opened interface
        libusb_close(amp_hand);
        amp_hand = NULL;
        printf("amp stopped\n");
    }

    // stop using libusb
    libusb_exit(NULL);

    return 0;
}
